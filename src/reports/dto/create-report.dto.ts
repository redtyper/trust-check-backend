import { IsString, IsInt, Min, Max, IsIn, IsOptional, IsEmail, IsUrl, IsBoolean } from 'class-validator';

export class CreateReportDto {
  @IsString()
  @IsIn(['NIP', 'PHONE', 'PERSON', 'BANK_ACCOUNT'])
  targetType: string;

  @IsString()
  targetValue: string;

  @IsInt()
  @Min(1)
  @Max(5)
  rating: number;

  @IsString()
  reason: string;

  @IsString()
  comment: string;

  // OSINT
  @IsOptional()
  @IsEmail()
  reportedEmail?: string;

  @IsOptional()
  @IsUrl()
  facebookLink?: string;

  @IsOptional()
  @IsString()
  screenshotUrl?: string; // URL z FB

  @IsOptional()
  @IsString()
  screenshotPath?: string; // NOWE - ścieżka pliku po uploadzie

  @IsOptional()
  @IsString()
  scammerName?: string;

  @IsOptional()
  @IsString()
  bankAccount?: string;

  @IsOptional()
  @IsBoolean()
  isAutoGenerated?: boolean;

  @IsOptional()
  @IsUrl()
  sourceUrl?: string;
}
